package com.jshx.qyaqsccn.entity;

import java.sql.Blob;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

import org.hibernate.annotations.GenericGenerator;

import com.jshx.core.base.entity.BaseModel;


/**
 * 实体类模板（目前仅适配MS-SQLServer数据库）
 * @author
 *
 */
@SuppressWarnings("serial")
@Entity
@Table(name="PRO_COM")
public class ProCom extends BaseModel
{
	/**
	 * 部门代码
	 */
	private String deptId;

	/**
	 * 删除标记
	 */
	private Integer delFlag;

	
	/**
	 * 所在区域id
	 */
	private String areaId;

	/**
	 * 所在区域名称
	 */
	private String areaName;

	/**
	 * 企业id
	 */
	private String companyId;

	/**
	 * 企业名称
	 */
	private String companyName;

	/**
	 * 承诺内容
	 */
	private String commitmentContent;

	/**
	 * 备注
	 */
	private String remark;

	/**
	 * 关联附件id
	 */
	private String linkId;
	
	/**
	 * 年度
	 */
	private Date commitmentYear;

	
	public ProCom(){
	}
	
	public ProCom(String id, String areaId, String companyName, String commitmentContent,String createUserID,Date commitmentYear){
this.id = id;

this.areaId = areaId;

this.companyName = companyName;

this.commitmentContent = commitmentContent;
this.createUserID=createUserID;
this.commitmentYear = commitmentYear;
}


	@Column
	public String getDeptId()
	{
		return deptId;
	}

	public void setDeptId(String deptId)
	{
		this.deptId = deptId;
	}

	@Column
	public Integer getDelFlag()
	{
		return delFlag;
	}

	public void setDelFlag(Integer delFlag)
	{
		this.delFlag = delFlag;
	}

	
	@Column(name="AREA_ID")
	public String getAreaId()
	{
		return this.areaId;
	}

	public void setAreaId(String areaId)
	{
		this.areaId = areaId;
	}

	@Column(name="AREA_NAME")
	public String getAreaName()
	{
		return this.areaName;
	}

	public void setAreaName(String areaName)
	{
		this.areaName = areaName;
	}

	@Column(name="COMPANY_ID")
	public String getCompanyId()
	{
		return this.companyId;
	}

	public void setCompanyId(String companyId)
	{
		this.companyId = companyId;
	}

	@Column(name="COMPANY_NAME")
	public String getCompanyName()
	{
		return this.companyName;
	}

	public void setCompanyName(String companyName)
	{
		this.companyName = companyName;
	}

	@Column(name="COMMITMENT_CONTENT")
	public String getCommitmentContent()
	{
		return this.commitmentContent;
	}

	public void setCommitmentContent(String commitmentContent)
	{
		this.commitmentContent = commitmentContent;
	}

	@Column(name="REMARK")
	public String getRemark()
	{
		return this.remark;
	}

	public void setRemark(String remark)
	{
		this.remark = remark;
	}

	@Column(name="LINK_ID")
	public String getLinkId()
	{
		return this.linkId;
	}

	public void setLinkId(String linkId)
	{
		this.linkId = linkId;
	}
	@Column(name="COMMITMENT_YEAR")
	public Date getCommitmentYear() {
		return commitmentYear;
	}

	public void setCommitmentYear(Date commitmentYear) {
		this.commitmentYear = commitmentYear;
	}

}
